def format_model_size_bytes(n: int) -> str:
    """Человекочитаемый размер скачиваемого .bin (MiB / GiB)."""
    if n <= 0:
        return "?"
    if n < 1024 * 1024:
        return f"{n / 1024:.0f} KiB"
    mib = n / (1024 * 1024)
    if mib >= 1024:
        return f"{mib / 1024:.2f} GiB"
    if mib >= 100:
        return f"{mib:.0f} MiB"
    return f"{mib:.1f} MiB"


def model_download_url(key: str) -> str:
    return f"{HF_BASE}/ggml-{key}.bin"
